const { DataTypes } = require("sequelize");

module.exports = {
    name: 'Language',
    define: (sequelize) => {
        sequelize.define('Language', {
            id: {
                type: DataTypes.UUID,
                primaryKey: true,
                defaultValue: DataTypes.UUIDV4, // Puedes usar una funci√≥n para generar UUIDs aleatorios
                allowNull: false
            },
            language: {
                type: DataTypes.STRING,
                allowNull: false
            },
            state: {
                type: DataTypes.BOOLEAN,
                allowNull: false,
                defaultValue: true,
            }
        });
    }
}